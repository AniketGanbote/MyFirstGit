
public class ArithmeticOperator {

	public static void main(String[] args) {
		
		// JAVA Arithmetic Operators 
		
		int n1 = 10, n2 = 5;
		
		System.out.println("1. Addition : " + (n1+n2));
		
		System.out.println("2. Subtraction : " + (n1-n2));
		
		System.out.println("3. Multiplication : " + (n1*n2));
		
		System.out.println("4. Division : " + (n1/n2));
		
		System.out.println("5. Modulo (Remainder) : " + (n1%n2));
		
		// JAVA Arithmetic Operators : Expression
		
		System.out.println("\nResult of 10*10/5+3-1*4/2 : " + (10*10/5+3-1*4/2));

	}

}
